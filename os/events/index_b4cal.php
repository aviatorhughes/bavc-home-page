<h3>One-Day Courses</h3>
<br />

<p><u>One-Day Courses</u> are a wonderful opportunity to refresh and strengthen your practice, or to give Dhamma service. One-day sittings at the Bay Area halls are often conducted by an assistant teacher, who is available for questions during noon interviews. You may bring your own meditation cushions, or borrow some from the center.</p>
