<p>The San Francisco Vipassana Hall serves the <b>old students</b> of S. N. Goenka in the San Francisco area by hosting one-hour sittings, special events and potlucks, as well as one-day courses. The hall is staffed and supported by grateful old students on a volunteer basis.</p>
<br />
<p>All meditation events are only for <u>old students</u>. Old students are those who have completed a 10-day Vipassana Meditation course with S.N. Goenka or his assistant teachers.</p>

