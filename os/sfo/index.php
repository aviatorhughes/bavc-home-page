<h2>San Francisco Vipassana Activities - 2 Halls</h2>

<br />
<p>
The new San Francisco Vipassana Hall downtown and a rented facility near Golden Gate Park both serve old students of S. N. Goenka in the Bay Area by hosting one-hour sittings and one-day courses. These locations are staffed and supported by grateful old students on a volunteer basis.
</p>
<br/>
<div style="text-align: center;" id="n4um">
  <img width="516" height="217" src="sfo_hall.jpg" alt="San Francisco Hall">
</div>

<br />
<p><b>Group sittings and one day courses</b> are open to anyone who has completed a 10 day course of Vipassana meditation as taught by S.N. Goenka. Both provide valuable opportunities to strengthen your practice of Vipassana by meditating with fellow students. Pre-registration is not necessary.</p>

