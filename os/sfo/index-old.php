<h2>San Francisco Vipassana Activities</h2>

<br />
<p>
We currently rent a facility near Golden Gate Park on a weekly basis in San Francisco to host one-hour sittings and one-day courses. The hall is staffed and supported by grateful old students on a volunteer basis.
</p>
<br/>
<div style="text-align: center;" id="n4um">
  <img width="516" height="217" src="sfo_hall.jpg" alt="San Francisco Hall">
</div>

<br />
<p>All meditation events are only for <u>old students</u>. Old students are those who have completed a 10-day Vipassana Meditation course with S.N. Goenka or his assistant teachers.</p>

<hr />

If it is a cold day, we will meditate in the smaller room, using heaters if necessary. Please bring warm blankets to use as shawls to stay warm during the course. Please call 415-728-3321 if you would either like a ride or want to offer a ride.
<br>

<br>
<b>New Update: Search for the San Francisco Vipassana Hall</b><br>

A group of old students in San Francisco is currently searching for a dedicated San Francisco Vipassana Hall for regular one-hour group sittings, special events and potlucks, as well as one-day courses. The location is not set but will likely be more accessible to many old students e.g. the Mission District, SOMA, etc.<br><br>

If you are interested in staying informed as this effort continues, or in helping with this effort, please fill out the following form:
