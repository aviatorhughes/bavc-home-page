<br />

<p>
To learn about group sittings and other events in San Francisco, you can join the email list by sending a blank email <a href="mailto:announce-sf-subscribe@mahavana.dhamma.org">announce-sf-subscribe@mahavana.dhamma.org</a>. Typically you will receive one email every week telling you about 1-Day courses and 3-Hour sittings.
</p>

