Group sittings are open to anyone who has completed a 10 day course of Vipassana meditation as taught by S.N. Goenka. Pre-registration is not necessary. <br><br>

The new location for the group sittings is the SF Vipassana Hall (SFVH). You can view a video of the space and take an optional survey <a href="https://docs.google.com/forms/d/e/1FAIpQLScZnoEPVYMa5D_eOnSfoBRzre03D3froQPpmF_5eD6cX82QRQ/viewform">here.</a> <br><br>

