Over the past year, the search committee has evaluated over 30 properties in Santa Clara, Benito and Santa Cruz counties with the goal of finding a property that would be suitable for developing a Vipassana meditation center for 150 students and servers. <br><br>

In September 2016, the Search Committee found a property in south Santa Clara County near Gilroy that looks promising.
<ul>
	<li>
		The property is 35 miles from San Jose airport and 8 miles from the Gilroy Caltrain station.  
	</li>
	<li>It is located on Redwood Retreat Road in a rural area bordered by an open space reserve (limited development) and by Mt Madonna county park. 
	</li>
	<li>There are three 20-acre parcels all owned by the same owner. While just one of these is currently on the market, there are strong indications the owner is willing to sell all three.
	</li>
</ul>

<br>

The Search Committee and Trust is in the process of investigating the property to determine the feasibility of developing a center for 150 students and servers and is preparing to make a contingency offer sometime in March 2017.  An initial assessment indicates this may be a viable property for our use, but there are many factors still to be considered to be sure this property will work for us. <b> Among those considerations, before we can consider an offer, is the need to assess our financial ability to make a purchase. </b>  <br><br>	


<b>Finances and development phases:</b>
<ul>
<li>
For a center for 150 participants, the ideal scenario is to purchase all three 20 acre lots.
</li>
<li>
The lot currently on the market is listed for $1.4 million. We are cautiously optimistic that we may be able to negotiate a purchase price of $3 million for <b>all three lots</b>.  
</li>
<li>
If we are able to purchase all three lots , the plan is to develop the middle lot starting sometime in 2018.  We would like to develop this initial phase to accommodate between 60 to 80 students. We think the cost of this initial development would be around $3M.  With the land purchase, the total would therefore be approximately $6 million for this first phase.
</li>
<li>
Ultimately, the goal would be to expand to the capacity of the center to accommodate a total of 150 students, servers and  teachers which would be done in future development phases as donations became available.</li>
<li>
The Trust currently has sufficient funds for the initial costs of getting approvals from the county for the project and pay for an option on the land.  The purchase of the property and subsequent development will need to be covered by one time or monthly donations or loans from old students or their families.</li>
 <li>
However, monthly donations alone won’t be able to cover acquisition costs unless the seller is willing to take back paper on the purchase.
</li>
</ul>
<br>

<b>Next Steps</b>
<ul>
	<li>There are several significant hurdles for this project to come to fruition:
		<ul>
			<li>
			Further evaluation is needed to determine that the property is suitable for a center with 150 participants and whether or not it is likely that the county and neighbors would approve of the project. 
			</li>
			<li>
				There needs to be sufficient financial support from old students. 
			</li>
			<li>The owner must accept an offer contingent on getting county approvals. (A 9 - 12 month process.) We may have to make a monthly payment to “hold” the property.
			</li>
			<li>The county needs to approve a conditional use permit that would allow the construction of a meditation center for 150 students and servers.
			</li>
		</ul>
	</li>
</ul>

