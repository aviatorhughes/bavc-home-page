<b>DHAMMA SERVICE</b><br>
It is old students like you who make it possible for hundreds of new students each year to benefit from Vipassana meditation as you did. To apply to serve on a course you can access the service application through the schedule page on each meditation center’s website.<br><br>

Please note that it is possible to serve any part of a 10-day course depending on space availability. Priority is given to full-time servers.<br><br>

Many students find that giving Dhamma service results in the strengthening of their own practice. Ways one can serve are described at:
<a href="http://www.manda.dhamma.org/old/WaysToServe.html">http://www.manda.dhamma.org/old/WaysToServe.html</a>
<br><br>
For inquiries specific to Occidental course, please write to <a href="info@sfbayarea.us.dhamma.org">info@sfbayarea.us.dhamma.org</a> or call us at (631)-629-6734.<br>

We encourage you to establish your daily practice, which is the key to success. 
We hope this information will support you in your efforts. Please let us know if you have any questions.<br/><br/>

