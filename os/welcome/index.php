 please Email them to <a href="mailto:at-questions@sfbayarea.us.dhamma.org">at-questions@sfbayarea.us.dhamma.org</a>.
  Please, include your phone number and the best time to reach you. 
  Depending on their relevance for a general audience, questions and answers 
  may also be published in the "Ask an Assistant Teacher" column in the
   triannual California newsletter, of course without any personally identifying details. 

