<p><b>Group sittings</b> provide valuable opportunities to strengthen your practice of Vipassana by sitting with fellow students. These group sittings and 1-Day courses are open to  those students who have completed at least one 10-Day course with S.N. Goenka or one of his assistant teachers.</p>
<br><p>To see the timetable for a 1-Day course, please visit the <a href="http://www.mahavana.dhamma.org/public/courses/oneday/course-desc.htm">One-Day Old Student Vipassana Meditation Course</a> page on the California Vipassana Center website.</p>
<br />
<p><b>Trust meeting</b> sessions includes an AT-led group sitting (9:00am - 10:00am) followed by a 2-hour CVC Trust meeting. All old students are <a href="http://www.mahavana.dhamma.org/os/trust/trust_sched.htm">welcome</a> to attend the group sitting and/or trust meeting.</p>

