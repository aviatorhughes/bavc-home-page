<p>Bay Area Vipassana Center (BAVC) Trust was formed in early 2014. In addition to being a hub for Bay Area old student courses and dhamma halls, it oversees the organization of the annual non-center 10-day course held over winter holidays in Occidental, CA. It also is the hub for North American Children’s Course Community Committee and Bay Area children’s courses. Lastly, but importantly, it will also be responsible for a dhamma center in the Bay Area if one is developed.</p>
<br />

<p>Committees on the trust include:</p>
<ul>
  <li>Finance Committee</li>
  <li>Outreach Committee</li>
  <li>Center Search Committee</li>
  <li>Children's Courses Committee</li>
  <li>North American Children's Course Community Committee</li>
  <li>South Bay Vipassana Hall Committee</li>
  <li>East Bay Vipassana Hall Committee</li>
  <li>Occidental Non-center Course Committee</li>
</ul>
<br />

<p>Trust meetings take place approximately every 2-3 months at a Bay Area location.  All old students are welcome to attend and observe trust meetings.  There are many opportunities to support committees and trust activities without becoming a trust member.  To learn about trust meetings times and locations, please view the <a href="/os/trust/trust_sched.php">Trust Meetings Schedule</a> page.</p> 
