<h2>BAVC Board of Directors (Trust)</h2>
<br />

<p>The Bay Area Vipassana Center (BAVC) organizes children’s courses and old student activities in the San Francisco Bay Area. One-hour sittings, three-hour sittings, one-day courses, service opportunities and trust meetings take place at homes of old students, two halls and other facilities in San Francisco, Berkeley, Santa Clara, and neighboring towns. The activities and halls are staffed and supported by grateful old students on a volunteer basis.</p>
<br />

<p>List of BAVC <a href="trustees">Board of Directors (Trustees)</a></p>
